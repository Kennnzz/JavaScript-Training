// Let variable. 'Let' is able to be overwritten
let age = 25;
let year = 2019;

// More than 1 variable in a log
console.log(age, year);

// age does not need let
// as it's alread been declared
age = 30;
console.log(age);

// Const variable is unable to be overwritten
const points = 100;
console.log(points);

var score = 75;
console.log(score);